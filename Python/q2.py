# WAP to find intersection of two lists:

#Defining two empty lists to store data :
list1 = [1,2,3]
list2 = [2,4,5]

#Defining empty list to store common data :
list3 = []

#Checking if item is in both the lists :

for items in list1 and list2 :
    if items in list1 and list2:
        list3.append(items)    #Will append item only when in both the lists.  

print(list3)            